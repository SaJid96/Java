import java.util.*;
public class AssignmentOperators {
    public static void main(String[] args) {
        int A=10;
        A+=10;
        System.out.println(A);

        int B=10;
        B-=5;
        System.out.println(B);

        int C=10;
        C*=11;
        System.out.println(C);

        int D=10;
        D/=5;
        System.out.println(D);

        int E=10;
        E%=3;
        System.out.println(E);

    }
}
